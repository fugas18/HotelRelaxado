   private String[] authenticateUser(String login, String senha) {
    String query = "SELECT nome, cargo FROM funcionarios WHERE utilizador = ? AND senha = ?";
    PreparedStatement stmt = null;
    ResultSet rs = null;
    MyConnection myconn = new MyConnection();

    try {
        Connection conn = myconn.getConnection();
        stmt = conn.prepareStatement(query);
        stmt.setString(1, login);
        stmt.setString(2, senha);                                                                                                                                                                               //Projeto Desenvolvido por Guilherme Martins e Hugo Oliveira
        rs = stmt.executeQuery();

        if (rs.next()) {
            String nome = rs.getString("nome");
            String cargo = rs.getString("cargo");
            return new String[]{nome, cargo};
        }
    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
        try {
            if (rs != null) rs.close();
            if (stmt != null) stmt.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    return null;


    }//GEN-LAST:event_btnEntrarActionPerformed

